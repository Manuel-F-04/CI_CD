package org.ci_cd;

public class Calculator {
    public static long add(long a, long b){
        return a + b;
    }

    public static long substract(long a, long b){
        return a - b;
    }

    public static long multiple(long a, long b){
        return a * b;
    }

    public static long divide(long a, long b){
        return a / b;
    }
}